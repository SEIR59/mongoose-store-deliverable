////////////////////////////////////////
// Import Dependencies
////////////////////////////////////////
const express = require("express");
const Product = require("../models/product");

/////////////////////////////////////////
// Create Route
/////////////////////////////////////////
const router = express.Router();

/////////////////////////////////////////
// Routes
/////////////////////////////////////////
router.use("/", (req, res) => {
  Product.find({})
    .then((products) => {
      res.render('products/index', {
        products
      })
    })
    .catch((error) => {
      console.log(error)
      res.json({ error })
    })

})


//////////////////////////////////////////
// Export the Router
//////////////////////////////////////////
module.exports = router;
